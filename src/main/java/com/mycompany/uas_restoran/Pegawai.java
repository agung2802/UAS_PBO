    private void tableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDataMouseClicked
        // TODO add your handling code here:
        btn_simpan.setEnabled(false);
        btn_edit.setEnabled(true);
        btn_hapus.setEnabled(true);
        int i = tableData.getSelectedRow();

        if (i == -1) {
            return;
        }

        String nama_karyawan = (String) model.getValueAt(i, 1);
        txt_nama.setText(nama_karyawan);
        String role = (String) model.getValueAt(i, 2);
        cb_role.setSelectedItem(role);
        String id = (String) model.getValueAt(i, 0);
        txt_id.setText(id);
        String password = (String) model.getValueAt(i, 3);
        txt_password.setText(password);
    }//GEN-LAST:event_tableDataMouseClicked

    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        if(txt_nama.getText().equals("") || txt_password.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "Restoran", JOptionPane.INFORMATION_MESSAGE);
        }else{
            String nama_karyawan = txt_nama.getText();
            String password = txt_password.getText();
            String role = (String) cb_role.getSelectedItem();
            try {
                long millis=System.currentTimeMillis();
                java.sql.Date date=new java.sql.Date(millis);
                System.out.println(date);
                String tgl = date.toString();
                Connection c = Koneksi.getKoneksi();
                String sql = "INSERT INTO pegawai VALUES (?, ?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, nama_karyawan);
                p.setString(2, role);                
                p.setString(3, null);
                p.setString(4, password);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan", "Elektronik Berkah", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                System.out.println("Terjadi Error: "+ e);
            } finally {
                loadData();
                txt_nama.setText("");
                txt_password.setText("");
                cb_role.setSelectedIndex(0);
            }
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

