    private void txt_cariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_cariKeyReleased
        // TODO add your handling code here:
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "select * from pegawai where id_pegawai like '%" + txt_cari.getText() + 
                    "%' or nama_pegawai like'%" + txt_cari.getText() + "%'" +
                    "or role like'%" + txt_cari.getText() + "%'";
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                Object[] o = new Object[4];
                o[0] = r.getString("id_pegawai");
                o[1] = r.getString("nama_pegawai");
                o[2] = r.getString("role");
                o[3] = r.getString("password");
                model.addRow(o);

            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error: "+ e);
        }
    }//GEN-LAST:event_txt_cariKeyReleased

