    private void txt_cariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_cariKeyReleased
        // TODO add your handling code here:
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            String best = null;
            if (txt_cari.getText().equals("true")) {
                best = "1";
            } else if(txt_cari.getText().equals("false")){
                best = "0";
            }
            String sql = "select * from menu where id_menu like '%" + txt_cari.getText() + 
                    "%' or nama_menu like'%" + txt_cari.getText() + "%'" +
                    "or bestseller like'%" + best + "%'" +
                    "or total_stok like'%" + txt_cari.getText() + "%'" +
                    "or harga_satuan like'%" + txt_cari.getText() + "%'" ;
            ResultSet r = s.executeQuery(sql);
            while (r.next()) {
                Object[] o = new Object[5];
                o[0] = r.getString("id_menu");
                o[1] = r.getString("nama_menu");
                if(r.getString("bestseller").equals("1")){
                    o[2] = "true";
                } else {
                    o[2] = "false";
                }
                o[3] = r.getString("total_stok");
                o[4] = r.getString("harga_satuan");
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error: "+ e);
        }
    }//GEN-LAST:event_txt_cariKeyReleased

