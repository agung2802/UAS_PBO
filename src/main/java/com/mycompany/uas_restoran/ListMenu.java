    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
        if(txt_nama.getText().equals("") || txt_total.getText().equals("") ||
                txt_harga.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "Restoran", JOptionPane.INFORMATION_MESSAGE);
        }else{
            int i = tableData.getSelectedRow();
            if (i == -1) {
                return;
            }
            String menu = (String) model.getValueAt(i, 0);
            try {
                Connection c = Koneksi.getKoneksi();

                Boolean best = cb_best.isSelected();
                String bestseller;
                if(best){
                    bestseller = "1";
                } else {
                    bestseller = "0";
                }
                String sql = "UPDATE menu SET nama_menu = '" + txt_nama.getText() + 
                        "', bestseller='"+ bestseller+ 
                        "', total_stok = '" + txt_total.getText() +
                        "', harga_satuan = '" + txt_harga.getText() +
                        "'  WHERE id_menu =" + txt_id.getText();
                PreparedStatement p = c.prepareStatement(sql);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null, "Data berhasil diubah", "Restoran", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                System.out.println("Terjadi Error: "+ e);
            } finally {
                loadData();
                txt_nama.setText("");
                cb_best.setSelected(false);
                txt_total.setText("");
                txt_harga.setText("");
                btn_simpan.setEnabled(true);
            }
        }
    }//GEN-LAST:event_btn_editActionPerformed

