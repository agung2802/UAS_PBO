    private void tableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDataMouseClicked
        // TODO add your handling code here:
        btn_simpan.setEnabled(false);
        btn_edit.setEnabled(true);
        btn_hapus.setEnabled(true);
        int i = tableData.getSelectedRow();

        if (i == -1) {
            return;
        }

        String nama_menu = (String) model.getValueAt(i, 1);
        txt_nama.setText(nama_menu);
        String best = (String) model.getValueAt(i, 2);
        System.out.println(best + " Best");
        if (best.equals("true")){
            cb_best.setSelected(true);
        } else {
            cb_best.setSelected(false);
        }
        String id = (String) model.getValueAt(i, 0);
        txt_id.setText(id);
        String total = (String) model.getValueAt(i, 3);
        txt_total.setText(total);
        String harga = (String) model.getValueAt(i, 4);
        txt_harga.setText(harga);
    }//GEN-LAST:event_tableDataMouseClicked

